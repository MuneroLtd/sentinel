// ---------------------------------------------------------------------------
// Drawing functions
// ---------------------------------------------------------------------------

void font_draw_char(uint16_t x, uint16_t y, char c,
                    sentinel::ui::Color fg, sentinel::ui::Color bg)
{
    if (c < 32 || c > 126) c = '?';
    const uint8_t* col_data = font_6x8[static_cast<uint8_t>(c) - 32];

    for (int col = 0; col < FONT_W; ++col) {
        uint8_t bits = col_data[col];
        for (int row = 0; row < FONT_H; ++row) {
            // bit 7 = top pixel
            sentinel::ui::Color px = (bits & (0x80u >> row)) ? fg : bg;
            lcd_draw_pixel(static_cast<uint16_t>(x + col),
                           static_cast<uint16_t>(y + row), px);
        }
    }
}

void font_draw_str(uint16_t x, uint16_t y, const char* s,
                   sentinel::ui::Color fg, sentinel::ui::Color bg)
{
    if (!s) return;
    uint16_t cx = x;
    while (*s) {
        font_draw_char(cx, y, *s, fg, bg);
        cx = static_cast<uint16_t>(cx + FONT_W);
        ++s;
    }
}

void font_draw_strf(uint16_t x, uint16_t y,
                    sentinel::ui::Color fg, sentinel::ui::Color bg,
                    const char* fmt, ...)
{
    char buf[128];
    va_list ap;
    va_start(ap, fmt);
    vsnprintf(buf, sizeof(buf), fmt, ap);
    va_end(ap);
    font_draw_str(x, y, buf, fg, bg);
}
